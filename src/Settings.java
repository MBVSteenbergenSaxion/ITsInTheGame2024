/** Settings Class for the whole canvas
 * */
public class Settings {

    /** Height, the standard height of the canvas
     * */
    public static int height = 768;

    /** Width, the standard width of the canvas
     * */
    public static int width = 1024;

    /** The standard amount of milliseconds of the gameloop
     * */
    public static int ms = 0;

    /** The width of the button (It's one fourth of the canvas width)
     * */
    public static int buttonWidth = width / 4;

    /** The height of the button (It's one tenth of the canvas height)
     * */
    public static int buttonHeight = height / 10;

    /** The fontsize of text in a button (Fontsize is 25)
     * */
    public static int fontSize = 25;

}
